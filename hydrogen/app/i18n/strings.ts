export type TKey = keyof typeof dict.en;

export function translate(key: TKey, locale: Locale): string {
  return (dict[locale] as Record<string, string>)[key] ?? (dict.en as Record<string, string>)[key] ?? key;
}

export function useT() {
  const rootData = useRouteLoaderData("root") as { locale?: Locale } | undefined;
  const storeLocale = useLocaleStore((s) => s.locale);
  const locale: Locale = rootData?.locale ?? storeLocale;
  return (key: TKey) => translate(key, locale);
}
